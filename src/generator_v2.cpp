std::string generate_qu_metadata_source(const SystemDecl& system)
{
    std::ostringstream out;
    out << "#include \"qu_messages.hpp\"\n\n#include <array>\n\nnamespace statespec_generated::qu\n{\nnamespace\n{\n\n";
    std::size_t total_messages = 0;
    for (const auto& queue : system.queues)
    {
        total_messages += queue.messages.size();
        for (const auto& message : queue.messages)
        {
            const auto symbol = to_lower(queue.name + message.name);
            out << "constexpr std::array<PayloadFieldMetadata, " << message.payload_fields.size() << "> " << symbol << "_payload_fields{{\n";
            for (const auto& field : message.payload_fields)
            {
                out << "    PayloadFieldMetadata{\"" << field.name << "\", \"" << field.type << "\", \"" << cpp_type_for_statespec_type(field.type) << "\", " << bool_text(is_optional_type(field.type)) << "},\n";
            }
            out << "}};\n\n";
        }
    }
    out << "constexpr std::array<MessageMetadata, " << total_messages << "> all_messages{{\n";
    for (const auto& queue : system.queues)
    {
        for (const auto& message : queue.messages)
        {
            const auto symbol = to_lower(queue.name + message.name);
            out << "    MessageMetadata{\"" << queue.name << "\", \"" << message.name << "\", \"" << payload_struct_name(queue, message) << "\", \"" << optional_or_empty(message.idempotency_key) << "\", " << symbol << "_payload_fields.data(), " << symbol << "_payload_fields.size()},\n";
        }
    }
    out << "}};\n\n";
    for (const auto& queue : system.queues)
    {
        const auto symbol = to_lower(queue.name);
        out << "constexpr std::array<MessageMetadata, " << queue.messages.size() << "> " << symbol << "_messages{{\n";
        for (const auto& message : queue.messages)
        {
            const auto message_symbol = to_lower(queue.name + message.name);
            out << "    MessageMetadata{\"" << queue.name << "\", \"" << message.name << "\", \"" << payload_struct_name(queue, message) << "\", \"" << optional_or_empty(message.idempotency_key) << "\", " << message_symbol << "_payload_fields.data(), " << message_symbol << "_payload_fields.size()},\n";
        }
        out << "}};\n\n";
    }
    out << "constexpr std::array<QueueMetadata, " << system.queues.size() << "> all_queues{{\n";
    for (const auto& queue : system.queues)
    {
        const auto symbol = to_lower(queue.name);
        out << "    QueueMetadata{\"" << queue.name << "\", \"" << optional_or_empty(queue.namespace_name) << "\", \"" << optional_or_empty(queue.channel) << "\", \"" << optional_or_empty(queue.visibility_timeout) << "\", " << queue.max_attempts.value_or(0) << ", " << symbol << "_messages.data(), " << symbol << "_messages.size()},\n";
    }
    out << "}};\n\n} // namespace\n\n";
    out << "const QueueMetadata* queues()\n{\n    return all_queues.data();\n}\n\nstd::size_t queue_count()\n{\n    return all_queues.size();\n}\n\nconst MessageMetadata* messages()\n{\n    return all_messages.data();\n}\n\nstd::size_t message_count()\n{\n    return all_messages.size();\n}\n\nstd::string_view idempotency_key_field(std::string_view queue_name, std::string_view message_name)\n{\n    for (std::size_t i = 0; i < all_messages.size(); ++i)\n    {\n        const auto& message = all_messages[i];\n        if (message.queue_name == queue_name && message.message_name == message_name)\n        {\n            return message.idempotency_key;\n        }\n    }\n    return {};\n}\n\n";
    out << "} // namespace statespec_generated::qu\n";
    return out.str();
}

