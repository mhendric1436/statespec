std::string join_path(const std::string& left, const std::string& right)
{
    if (left.empty())
    {
        return right;
    }
    return left.back() == '/' ? left + right : left + "/" + right;
}

std::string output_root(const GenerateDecl& declaration, const GenerationOptions& options)
{
    if (options.out_override.has_value())
    {
        return *options.out_override;
    }
    if (declaration.out.has_value())
    {
        return *declaration.out;
    }
    return "generated/" + declaration.target;
}

std::string bool_text(bool value)
{
    return value ? "true" : "false";
}

std::string optional_or_empty(const std::optional<std::string>& value)
{
    return value.value_or("");
}

std::string optional_bool_or_empty(const std::optional<bool>& value)
{
    return value.has_value() ? bool_text(*value) : "";
}

bool is_optional_type(const std::string& type)
{
    return !type.empty() && type.back() == '?';
}

std::string strip_optional_suffix(const std::string& type)
{
    return is_optional_type(type) ? type.substr(0, type.size() - 1) : type;
}

std::string to_lower(std::string value)
{
    std::transform(
        value.begin(), value.end(), value.begin(),
        [](unsigned char ch) { return static_cast<char>(std::tolower(ch)); }
    );
    return value;
}

std::string cpp_type_for_statespec_type(const std::string& type)
{
    const auto optional = is_optional_type(type);
    const auto base = strip_optional_suffix(type);

    std::string cpp_type;
    if (base == "string" || base == "uuid" || base == "json")
    {
        cpp_type = "std::string";
    }
    else if (base == "bool")
    {
        cpp_type = "bool";
    }
    else if (base == "int" || base == "int32")
    {
        cpp_type = "std::int32_t";
    }
    else if (base == "int64" || base == "long")
    {
        cpp_type = "std::int64_t";
    }
    else if (base == "double" || base == "decimal")
    {
        cpp_type = "double";
    }
    else if (base == "timestamp")
    {
        cpp_type = "std::chrono::system_clock::time_point";
    }
    else if (base == "duration")
    {
        cpp_type = "std::chrono::seconds";
    }
    else
    {
        cpp_type = base;
    }

    return optional ? "std::optional<" + cpp_type + ">" : cpp_type;
}

std::string payload_struct_name(const QueueDecl& queue, const MessageDecl& message)
{
    return queue.name + message.name + "Payload";
}

void append_header(std::ostringstream& out, const SystemDecl& system, const std::string& target)
{
    out << "# Generated by StateSpec\n";
    out << "# target: " << target << "\n";
    out << "# system: " << system.name << "\n\n";
}

std::string generate_mt_manifest(const SystemDecl& system, const GenerateDecl& declaration)
{
    std::ostringstream out;
    append_header(out, system, declaration.target);
    out << "entities:\n";
    for (const auto& entity : system.entities)
    {
        out << "  - name: " << entity.name << "\n";
        out << "    keys:\n";
        for (const auto& key : entity.key_fields)
        {
            out << "      - " << key << "\n";
        }
        out << "    fields:\n";
        for (const auto& field : entity.fields)
        {
            out << "      - name: " << field.name << "\n";
            out << "        type: " << field.type << "\n";
            out << "        cpp_type: " << cpp_type_for_statespec_type(field.type) << "\n";
            out << "        optional: " << bool_text(is_optional_type(field.type)) << "\n";
        }
    }
    return out.str();
}

std::string generate_mt_entities_header(const SystemDecl& system)
{
    std::ostringstream out;
    out << "#pragma once\n\n";
    out << "#include <chrono>\n#include <cstddef>\n#include <cstdint>\n#include <optional>\n#include <string>\n#include <string_view>\n\n";
    out << "namespace statespec_generated::mt\n{\n\n";
    out << "struct FieldMetadata\n{\n    std::string_view name;\n    std::string_view statespec_type;\n    std::string_view cpp_type;\n    bool optional;\n};\n\n";
    out << "struct EntityMetadata\n{\n    std::string_view name;\n    const FieldMetadata* fields;\n    std::size_t field_count;\n    const std::string_view* keys;\n    std::size_t key_count;\n};\n\n";

    for (const auto& entity : system.entities)
    {
        out << "struct " << entity.name << "\n{\n";
        for (const auto& field : entity.fields)
        {
            out << "    " << cpp_type_for_statespec_type(field.type) << ' ' << field.name << "{};\n";
        }
        out << "};\n\n";
    }

    out << "const EntityMetadata* entities();\nstd::size_t entity_count();\n\n";
    out << "} // namespace statespec_generated::mt\n";
    return out.str();
}

std::string generate_mt_metadata_source(const SystemDecl& system)
{
    std::ostringstream out;
    out << "#include \"mt_entities.hpp\"\n\n#include <array>\n\n";
    out << "namespace statespec_generated::mt\n{\nnamespace\n{\n\n";
    for (const auto& entity : system.entities)
    {
        const auto symbol = to_lower(entity.name);
        out << "constexpr std::array<FieldMetadata, " << entity.fields.size() << "> " << symbol << "_fields{{\n";
        for (const auto& field : entity.fields)
        {
            out << "    FieldMetadata{\"" << field.name << "\", \"" << field.type << "\", \"" << cpp_type_for_statespec_type(field.type) << "\", " << bool_text(is_optional_type(field.type)) << "},\n";
        }
        out << "}};\n\n";
        out << "constexpr std::array<std::string_view, " << entity.key_fields.size() << "> " << symbol << "_keys{{\n";
        for (const auto& key : entity.key_fields)
        {
            out << "    \"" << key << "\",\n";
        }
        out << "}};\n\n";
    }
    out << "constexpr std::array<EntityMetadata, " << system.entities.size() << "> all_entities{{\n";
    for (const auto& entity : system.entities)
    {
        const auto symbol = to_lower(entity.name);
        out << "    EntityMetadata{\"" << entity.name << "\", " << symbol << "_fields.data(), " << symbol << "_fields.size(), " << symbol << "_keys.data(), " << symbol << "_keys.size()},\n";
    }
    out << "}};\n\n} // namespace\n\n";
    out << "const EntityMetadata* entities()\n{\n    return all_entities.data();\n}\n\n";
    out << "std::size_t entity_count()\n{\n    return all_entities.size();\n}\n\n";
    out << "} // namespace statespec_generated::mt\n";
    return out.str();
}

std::string generate_mt_state_machine_manifest(const SystemDecl& system)
{
    std::ostringstream out;
    append_header(out, system, "mt-state-machines");
    out << "state_machines:\n";
    for (const auto& entity : system.entities)
    {
        if (!entity.state_machine.has_value())
        {
            continue;
        }
        out << "  - entity: " << entity.name << "\n";
        out << "    initial: " << optional_or_empty(entity.state_machine->initial_state) << "\n";
        out << "    states:\n";
        for (const auto& state : entity.state_machine->states)
        {
            out << "      - " << state.name << "\n";
        }
        out << "    terminal_states:\n";
        for (const auto& state : entity.state_machine->terminal_states)
        {
            out << "      - " << state << "\n";
        }
        out << "    transitions:\n";
        for (const auto& transition : entity.state_machine->transitions)
        {
            out << "      - from: " << transition.from << "\n        to: " << transition.to << "\n";
        }
    }
    return out.str();
}

